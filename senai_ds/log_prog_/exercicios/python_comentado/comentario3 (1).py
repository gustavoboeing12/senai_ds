# Pede ao usuário a temperatura em Fahrenheit e a armazena como um número de ponto flutuante
fahrenheit = float(input("Digite a temperatura em Fahrenheit: "))

# Converte a temperatura de Fahrenheit para Celsius usando a fórmula de conversão
celsius = (fahrenheit - 32) * 5 / 9

# Imprime a temperatura convertida em Celsius
print("Temperatura em Celsius é:", celsius)
